name = "harry"
print(name[0:2]) #goes from 0 to 2-1
print(name[2:-1]) #goes from 2 to 4
#h    a   r   r   y
#-5  -4  -3  -2  -1
# 0   1   2   3   4
print(name[0:4:1]) #skips n-1 character i.e. 0 character here
print(name[0:4:2]) #skips n-1 character i.e. 1 character here
print(name[:4])#replace the 1st empty number with 0 i.e[0:4]
print(name[1:])#replace the last number with string length
